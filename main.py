import warnings
from contextlib import asynccontextmanager

warnings.filterwarnings(
    "ignore",
    message=".*Valid config keys have changed.*",
    category=UserWarning,
    module="pydantic.*",
)

from fastapi import FastAPI
from fastapi_pagination import add_pagination

from common.settings import Settings
from db.database import Database
from db import init_redis, close_redis
from services.users.routes.user import users_router
from services.amenities.routes.amenities import amenities_router
from services.apartaments.routes.apartaments import apartament_router
from services.bookings.routes.bookings import bookings_router


@asynccontextmanager
async def lifespan(app: FastAPI):
    database = Database(settings=Settings())
    await init_redis()
    yield
    await database.dispose(close=False)
    await close_redis()


app = FastAPI(lifespan=lifespan)


app.include_router(users_router)
app.include_router(amenities_router, tags=["amenities"])
app.include_router(apartament_router, tags=["apartaments"])
app.include_router(bookings_router, tags=["bookings"])
add_pagination(app)
